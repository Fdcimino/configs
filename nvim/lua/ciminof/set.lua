-- helper functions

function GetUndoDir()
    local dir = os.getenv("UserProfile")
    if dir ~= nil then
        return dir
    end
    -- linux undo tree dir
    dir = os.getenv("HOME")
    if dir ~= nil then
        return dir
    end
end

